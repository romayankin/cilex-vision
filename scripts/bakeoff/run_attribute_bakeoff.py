def log_run_to_mlflow(
    *,
    args: argparse.Namespace,
    candidate: AttributeCandidate,
    backend_name: str,
    metadata: DatasetMetadata,
    metrics: AttributeMetrics,
    git_revision: str | None,
    git_dirty: bool | None,
) -> str:
    mlflow = require_module("mlflow", "mlflow")
    mlflow.set_tracking_uri(args.mlflow_uri)
    mlflow.set_experiment(args.mlflow_experiment)

    with tempfile.TemporaryDirectory(prefix="attribute-bakeoff-") as temp_dir_name:
        temp_dir = Path(temp_dir_name)
        confusion_path = temp_dir / "confusion_matrix.json"
        per_color_path = temp_dir / "per_color_report.json"
        summary_path = temp_dir / "summary.md"

        confusion_path.write_text(
            json.dumps(
                {
                    "labels": list(COLOR_NAMES),
                    "matrix": metrics.confusion_matrix,
                },
                indent=2,
                sort_keys=True,
            )
            + "\n",
            encoding="utf-8",
        )
        per_color_path.write_text(
            json.dumps(
                {
                    "per_color_accuracy": metrics.per_color_accuracy,
                    "per_color_counts": metadata.per_color_counts,
                    "confusion_pairs": {
                        "black_blue": metrics.confusion_black_blue,
                        "white_silver": metrics.confusion_white_silver,
                        "brown_orange": metrics.confusion_brown_orange,
                    },
                },
                indent=2,
                sort_keys=True,
            )
            + "\n",
            encoding="utf-8",
        )
        summary_path.write_text(
            build_summary_markdown(
                candidate,
                backend_name,
                metadata,
                metrics,
                args.confidence_threshold,
            ),
            encoding="utf-8",
        )

        with mlflow.start_run(run_name=f"attribute-{candidate.name}") as run:
            mlflow.set_tags(
                {
                    "bakeoff.phase": "attribute",
                    "bakeoff.candidate": candidate.name,
                    "bakeoff.safe_default": str(candidate.is_safe_default).lower(),
                    "bakeoff.protocol_version": "1.0.0",
                }
            )
            params = {
                "candidate_name": candidate.name,
                "onnx_path": str(candidate.onnx_path),
                "triton_model_name": candidate.triton_model_name,
                "input_name": candidate.input_name,
                "output_name": candidate.output_name,
                "input_size": candidate.input_size,
                "confidence_threshold": args.confidence_threshold,
                "dataset_manifest": str(metadata.manifest_path),
                "source_project": metadata.source_project,
                "backend": backend_name,
                "batch_size": args.batch_size,
            }
            if metadata.export_timestamp is not None:
                params["dataset_export_timestamp"] = metadata.export_timestamp
            if args.triton_url.strip():
                params["triton_url"] = args.triton_url.strip()
            if git_revision is not None:
                params["git_revision"] = git_revision
            if git_dirty is not None:
                params["git_dirty"] = str(git_dirty).lower()
            mlflow.log_params(params)
            mlflow.log_metrics(
                {
                    "overall_accuracy": metrics.overall_accuracy,
                    "macro_per_color_accuracy": metrics.macro_per_color_accuracy,
                    "unknown_rate": metrics.unknown_rate,
                    "latency_p50_ms": metrics.latency_p50_ms,
                    "latency_p95_ms": metrics.latency_p95_ms,
                    "latency_p99_ms": metrics.latency_p99_ms,
                    "throughput_fps": metrics.throughput_fps,
                    "confusion_black_blue": metrics.confusion_black_blue,
                    "confusion_white_silver": metrics.confusion_white_silver,
                    "confusion_brown_orange": metrics.confusion_brown_orange,
                    "evaluated_crops": float(metrics.evaluated_crops),
                }
            )
            for color_name, value in metrics.per_color_accuracy.items():
                mlflow.log_metric(f"attr_accuracy_{color_name}", value)
            mlflow.log_artifact(str(confusion_path))
            mlflow.log_artifact(str(per_color_path))
            mlflow.log_artifact(str(summary_path))
            return run.info.run_id


def run_evaluation(args: argparse.Namespace) -> tuple[str, AttributeMetrics]:
    if args.batch_size <= 0:
        raise RuntimeError("--batch-size must be positive")
    candidate = resolve_candidate(args)
    records, metadata = load_manifest(args.manifest)
    backend_name, backend = select_backend(candidate, args.triton_url)
    metrics = evaluate_candidate(
        records,
        candidate,
        backend,
        confidence_threshold=args.confidence_threshold,
        batch_size=args.batch_size,
    )
    git_revision, git_dirty = detect_git_state()
    run_id = log_run_to_mlflow(
        args=args,
        candidate=candidate,
        backend_name=backend_name,
        metadata=metadata,
        metrics=metrics,
        git_revision=git_revision,
        git_dirty=git_dirty,
    )
    args.output_dir.mkdir(parents=True, exist_ok=True)
    local_summary_path = args.output_dir / f"{candidate.name}_summary.json"
    local_summary_path.write_text(
        json.dumps(
            {
                "candidate": candidate.name,
                "backend": backend_name,
                "dataset": {
                    "manifest_path": str(metadata.manifest_path),
                    "export_timestamp": metadata.export_timestamp,
                    "source_project": metadata.source_project,
                    "total_crops": metadata.total_crops,
                    "per_color_counts": metadata.per_color_counts,
                },
                "metrics": metrics_to_json_dict(metrics),
                "mlflow_run_id": run_id,
            },
            indent=2,
            sort_keys=True,
        )
        + "\n",
        encoding="utf-8",
    )
    print(
        json.dumps(
            {
                "candidate": candidate.name,
                "backend": backend_name,
                "overall_accuracy": round(metrics.overall_accuracy, 6),
                "macro_per_color_accuracy": round(
                    metrics.macro_per_color_accuracy, 6
                ),
                "unknown_rate": round(metrics.unknown_rate, 6),
                "latency_p95_ms": round(metrics.latency_p95_ms, 3),
                "throughput_fps": round(metrics.throughput_fps, 3),
                "mlflow_run_id": run_id,
            },
            indent=2,
            sort_keys=True,
        )
    )
    return run_id, metrics


def main() -> None:
    run_evaluation(parse_args())


