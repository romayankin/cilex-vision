"""Run the attribute classifier bake-off and log results to MLflow.

Usage:
    python run_attribute_bakeoff.py --manifest data/eval/attribute/manifest.json \
        --candidate resnet18 \
        --onnx-path artifacts/models/attribute/resnet18.onnx \
        --triton-url localhost:8001 \
        --mlflow-uri http://localhost:5000
"""

