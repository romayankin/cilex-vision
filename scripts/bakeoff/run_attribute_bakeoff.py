COLOR_NAMES: tuple[str, ...] = (
    "red",
    "blue",
    "white",
    "black",
    "silver",
    "green",
    "yellow",
    "brown",
    "orange",
    "unknown",
)
SAFE_DEFAULT_ATTRIBUTE = "resnet18"
DEFAULT_EXPERIMENT = "attribute-bakeoff"
DEFAULT_BATCH_SIZE = 32
COLOR_TO_INDEX = {color_name: index for index, color_name in enumerate(COLOR_NAMES)}


@dataclass(frozen=True)
class AttributeCandidate:
    name: str
    onnx_path: Path
    triton_model_name: str
    input_name: str = "images"
    output_name: str = "probabilities"
    input_size: int = 224
    is_safe_default: bool = False


@dataclass(frozen=True)
class DatasetMetadata:
    manifest_path: Path
    export_timestamp: str | None
    source_project: str
    total_crops: int
    per_color_counts: dict[str, int]


@dataclass(frozen=True)
class CropRecord:
    crop_path: Path
    color_name: str
    object_class: str
    attribute_name: str
    source_image: str


@dataclass(frozen=True)
class LatencyStats:
    p50_ms: float
    p95_ms: float
    p99_ms: float


@dataclass(frozen=True)
class AttributeMetrics:
    overall_accuracy: float
    macro_per_color_accuracy: float
    unknown_rate: float
    latency_p50_ms: float
    latency_p95_ms: float
    latency_p99_ms: float
    throughput_fps: float
    evaluated_crops: int
    batch_size: int
    per_color_accuracy: dict[str, float]
    confusion_matrix: list[list[int]]
    confusion_black_blue: float
    confusion_white_silver: float
    confusion_brown_orange: float


