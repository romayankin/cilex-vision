def resolve_candidate(args: argparse.Namespace) -> AttributeCandidate:
    base = DEFAULT_CANDIDATES[args.candidate]
    return AttributeCandidate(
        name=base.name,
        onnx_path=args.onnx_path or base.onnx_path,
        triton_model_name=args.triton_model or base.triton_model_name,
        input_name=base.input_name,
        output_name=base.output_name,
        input_size=base.input_size,
        is_safe_default=base.is_safe_default,
    )


def detect_git_state() -> tuple[str | None, bool | None]:
    try:
        revision = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            check=True,
            capture_output=True,
            text=True,
        ).stdout.strip()
        dirty = bool(
            subprocess.run(
                ["git", "status", "--porcelain"],
                check=True,
                capture_output=True,
                text=True,
            ).stdout.strip()
        )
    except (FileNotFoundError, subprocess.CalledProcessError):
        return None, None
    return revision or None, dirty


def load_manifest(manifest_path: Path) -> tuple[list[CropRecord], DatasetMetadata]:
    if not manifest_path.exists():
        raise FileNotFoundError(f"evaluation manifest not found: {manifest_path}")
    payload = json.loads(manifest_path.read_text(encoding="utf-8"))
    colors = tuple(str(value) for value in payload.get("colors", ()))
    if colors != COLOR_NAMES:
        raise RuntimeError(
            "manifest colors must match the canonical 10-color vocabulary in order"
        )

    per_color_counts = {
        color_name: int(payload.get("per_color_counts", {}).get(color_name, 0))
        for color_name in COLOR_NAMES
    }
    missing_colors = [color for color, count in per_color_counts.items() if count <= 0]
    if missing_colors:
        raise RuntimeError(
            "evaluation manifest is incomplete; missing crops for colors: "
            + ", ".join(missing_colors)
        )

    items = payload.get("items")
    if not isinstance(items, list) or not items:
        raise RuntimeError("evaluation manifest contains no crop items")

    records: list[CropRecord] = []
    for item in items:
        raw_path = item.get("crop_path")
        color_name = str(item.get("color", "")).strip().lower()
        if not raw_path:
            raise RuntimeError("manifest crop item is missing crop_path")
        if color_name not in COLOR_NAMES:
            raise RuntimeError(f"manifest crop item has invalid color label: {color_name!r}")
        crop_path = Path(raw_path)
        if not crop_path.is_absolute():
            crop_path = (manifest_path.parent / crop_path).resolve()
        if not crop_path.exists():
            raise FileNotFoundError(f"crop referenced by manifest does not exist: {crop_path}")
        records.append(
            CropRecord(
                crop_path=crop_path,
                color_name=color_name,
                object_class=str(item.get("object_class", "")),
                attribute_name=str(item.get("attribute_name", "")),
                source_image=str(item.get("source_image", "")),
            )
        )

    total_crops = int(payload.get("total_crops", 0))
    if total_crops != len(records):
        raise RuntimeError(
            "manifest total_crops does not match the number of crop items "
            f"({total_crops} != {len(records)})"
        )

    metadata = DatasetMetadata(
        manifest_path=manifest_path,
        export_timestamp=str(payload.get("export_timestamp"))
        if payload.get("export_timestamp") is not None
        else None,
        source_project=str(payload.get("source_project", "unknown")),
        total_crops=total_crops,
        per_color_counts=per_color_counts,
    )
    return records, metadata


def load_batch_tensors(records: list[CropRecord], input_size: int) -> Any:
    image_module = require_module("PIL.Image", "Pillow")
    numpy = require_module("numpy", "numpy")
    tensors: list[Any] = []
    for record in records:
        with image_module.open(record.crop_path) as image:
            resized = image.convert("RGB").resize(
                (input_size, input_size),
                image_module.BILINEAR,
            )
            tensor = numpy.asarray(resized, dtype=numpy.float32) / 255.0
            if tensor.ndim != 3 or tensor.shape[2] != 3:
                raise RuntimeError(f"unexpected crop shape for {record.crop_path}")
            tensors.append(tensor.transpose(2, 0, 1))
    return numpy.stack(tensors, axis=0)


class TritonClassifierBackend:
    def __init__(self, url: str, model_name: str, input_name: str, output_name: str) -> None:
        grpcclient = require_module("tritonclient.grpc", "tritonclient[grpc]")
        self._grpcclient = grpcclient
        self._client = grpcclient.InferenceServerClient(url=url)
        self._model_name = model_name
        self._input_name = input_name
        self._output_name = output_name
        self._verify_ready(url)

    def _verify_ready(self, url: str) -> None:
        try:
            if not self._client.is_server_live():
                raise RuntimeError(f"Triton server at {url} is not live")
            if not self._client.is_model_ready(self._model_name):
                raise RuntimeError(f"Triton model {self._model_name!r} is not ready")
        except Exception as exc:  # pragma: no cover - depends on external service
            raise RuntimeError(f"unable to use Triton backend at {url}: {exc}") from exc

    def infer_batch(self, batch: Any) -> Any:
        inputs = [
            self._grpcclient.InferInput(
                self._input_name,
                list(batch.shape),
                "FP32",
            )
        ]
        inputs[0].set_data_from_numpy(batch)
        outputs = [self._grpcclient.InferRequestedOutput(self._output_name)]
        try:
            result = self._client.infer(
                model_name=self._model_name,
                inputs=inputs,
                outputs=outputs,
            )
        except Exception as exc:  # pragma: no cover - depends on external service
            raise RuntimeError(f"Triton inference failed: {exc}") from exc
        output = result.as_numpy(self._output_name)
        if output is None:
            raise RuntimeError(
                f"Triton output {self._output_name!r} was not returned for {self._model_name!r}"
            )
        return output


class OnnxRuntimeClassifierBackend:
    def __init__(self, onnx_path: Path, input_name: str, output_name: str) -> None:
        if not onnx_path.exists():
            raise FileNotFoundError(f"ONNX model not found: {onnx_path}")
        onnxruntime = require_module("onnxruntime", "onnxruntime")
        self._session = onnxruntime.InferenceSession(
            str(onnx_path),
            providers=["CPUExecutionProvider"],
        )
        self._input_name = input_name
        self._output_name = output_name

    def infer_batch(self, batch: Any) -> Any:
        outputs = self._session.run([self._output_name], {self._input_name: batch})
        if not outputs:
            raise RuntimeError(f"ONNX runtime returned no outputs for {self._output_name!r}")
        return outputs[0]


def select_backend(
    candidate: AttributeCandidate,
    triton_url: str,
) -> tuple[str, ClassifierBackend]:
    triton_error: str | None = None
    if triton_url.strip():
        try:
            return (
                "triton",
                TritonClassifierBackend(
                    triton_url.strip(),
                    candidate.triton_model_name,
                    candidate.input_name,
                    candidate.output_name,
                ),
            )
        except Exception as exc:
            triton_error = str(exc)

    try:
        return (
            "onnxruntime",
            OnnxRuntimeClassifierBackend(
                candidate.onnx_path,
                candidate.input_name,
                candidate.output_name,
            ),
        )
    except Exception as exc:
        if triton_error is not None:
            raise RuntimeError(
                "no usable inference backend found; Triton error: "
                f"{triton_error}; ONNX Runtime error: {exc}"
            ) from exc
        raise


def softmax_rows(values: Any) -> Any:
    numpy = require_module("numpy", "numpy")
    shifted = values - numpy.max(values, axis=1, keepdims=True)
    exp_values = numpy.exp(shifted)
    return exp_values / numpy.sum(exp_values, axis=1, keepdims=True)


def decode_predictions(raw_output: Any, confidence_threshold: float) -> list[str]:
    numpy = require_module("numpy", "numpy")
    matrix = numpy.asarray(raw_output, dtype=numpy.float32)
    if matrix.ndim != 2 or matrix.shape[1] != len(COLOR_NAMES):
        raise RuntimeError(
            "classifier output must have shape [batch, 10] to match the color vocabulary"
        )
    probabilities = softmax_rows(matrix)
    named_probabilities = probabilities[:, : len(COLOR_NAMES) - 1]
    best_indices = numpy.argmax(named_probabilities, axis=1)
    best_confidences = named_probabilities[numpy.arange(len(best_indices)), best_indices]
    predictions: list[str] = []
    for best_index, confidence in zip(best_indices.tolist(), best_confidences.tolist()):
        if float(confidence) < confidence_threshold:
            predictions.append("unknown")
        else:
            predictions.append(COLOR_NAMES[int(best_index)])
    return predictions


def compute_latency_stats(samples_ms: list[float]) -> LatencyStats:
    numpy = require_module("numpy", "numpy")
    if not samples_ms:
        return LatencyStats(0.0, 0.0, 0.0)
    values = numpy.asarray(samples_ms, dtype=numpy.float64)
    return LatencyStats(
        p50_ms=float(numpy.percentile(values, 50)),
        p95_ms=float(numpy.percentile(values, 95)),
        p99_ms=float(numpy.percentile(values, 99)),
    )


def pair_confusion_rate(
    confusion_matrix: list[list[int]],
    left_color: str,
    right_color: str,
) -> float:
    left_index = COLOR_TO_INDEX[left_color]
    right_index = COLOR_TO_INDEX[right_color]
    pair_total = (
        sum(confusion_matrix[left_index]) + sum(confusion_matrix[right_index])
    )
    if pair_total == 0:
        return 0.0
    confused = (
        confusion_matrix[left_index][right_index]
        + confusion_matrix[right_index][left_index]
    )
    return confused / pair_total


def evaluate_candidate(
    records: list[CropRecord],
    candidate: AttributeCandidate,
    backend: ClassifierBackend,
    *,
    confidence_threshold: float,
    batch_size: int,
) -> AttributeMetrics:
    confusion_matrix = [[0 for _ in COLOR_NAMES] for _ in COLOR_NAMES]
    latency_samples_ms: list[float] = []
    inference_time_s = 0.0

    for start in range(0, len(records), batch_size):
        batch_records = records[start : start + batch_size]
        batch_tensor = load_batch_tensors(batch_records, candidate.input_size)
        started = time.perf_counter()
        raw_output = backend.infer_batch(batch_tensor)
        elapsed_s = time.perf_counter() - started
        inference_time_s += elapsed_s
        latency_samples_ms.append(elapsed_s * 1000.0)
        predictions = decode_predictions(raw_output, confidence_threshold)
        for record, prediction in zip(batch_records, predictions):
            true_index = COLOR_TO_INDEX[record.color_name]
            predicted_index = COLOR_TO_INDEX[prediction]
            confusion_matrix[true_index][predicted_index] += 1

    evaluated_crops = len(records)
    correct = sum(confusion_matrix[index][index] for index in range(len(COLOR_NAMES)))
    per_color_accuracy: dict[str, float] = {}
    for color_name, index in COLOR_TO_INDEX.items():
        row_total = sum(confusion_matrix[index])
        if row_total == 0:
            raise RuntimeError(
                f"evaluation manifest produced zero rows for required color {color_name!r}"
            )
        per_color_accuracy[color_name] = confusion_matrix[index][index] / row_total

    latency_stats = compute_latency_stats(latency_samples_ms)
    macro_per_color_accuracy = statistics.fmean(per_color_accuracy.values())
    unknown_predictions = sum(
        confusion_matrix[row_index][COLOR_TO_INDEX["unknown"]]
        for row_index in range(len(COLOR_NAMES))
    )
    throughput_fps = (
        evaluated_crops / inference_time_s if inference_time_s > 0.0 else 0.0
    )

    return AttributeMetrics(
        overall_accuracy=correct / evaluated_crops if evaluated_crops else 0.0,
        macro_per_color_accuracy=macro_per_color_accuracy,
        unknown_rate=unknown_predictions / evaluated_crops if evaluated_crops else 0.0,
        latency_p50_ms=latency_stats.p50_ms,
        latency_p95_ms=latency_stats.p95_ms,
        latency_p99_ms=latency_stats.p99_ms,
        throughput_fps=throughput_fps,
        evaluated_crops=evaluated_crops,
        batch_size=batch_size,
        per_color_accuracy=per_color_accuracy,
        confusion_matrix=confusion_matrix,
        confusion_black_blue=pair_confusion_rate(confusion_matrix, "black", "blue"),
        confusion_white_silver=pair_confusion_rate(
            confusion_matrix, "white", "silver"
        ),
        confusion_brown_orange=pair_confusion_rate(
            confusion_matrix, "brown", "orange"
        ),
    )


def build_summary_markdown(
    candidate: AttributeCandidate,
    backend_name: str,
    metadata: DatasetMetadata,
    metrics: AttributeMetrics,
    confidence_threshold: float,
) -> str:
    lines = [
        f"# Attribute Bake-Off Summary: {candidate.name}",
        "",
        f"- candidate: `{candidate.name}`",
        f"- backend: `{backend_name}`",
        f"- dataset manifest: `{metadata.manifest_path}`",
        f"- source project: `{metadata.source_project}`",
        f"- export timestamp: `{metadata.export_timestamp or 'unknown'}`",
        f"- evaluated crops: `{metrics.evaluated_crops}`",
        f"- batch size: `{metrics.batch_size}`",
        f"- confidence threshold: `{confidence_threshold:.2f}`",
        f"- overall accuracy: `{metrics.overall_accuracy:.4f}`",
        (
            "- macro per-color accuracy: "
            f"`{metrics.macro_per_color_accuracy:.4f}`"
        ),
        f"- unknown rate: `{metrics.unknown_rate:.4f}`",
        f"- latency p50 / p95 / p99 ms: "
        f"`{metrics.latency_p50_ms:.2f}` / "
        f"`{metrics.latency_p95_ms:.2f}` / "
        f"`{metrics.latency_p99_ms:.2f}`",
        f"- throughput crops/sec: `{metrics.throughput_fps:.2f}`",
        "",
        "## Per-Color Accuracy",
        "",
        "| Color | Accuracy |",
        "|-------|----------|",
    ]
    for color_name in COLOR_NAMES:
        lines.append(
            f"| {color_name} | {metrics.per_color_accuracy[color_name]:.4f} |"
        )
    return "\n".join(lines) + "\n"


