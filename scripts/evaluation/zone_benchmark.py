@dataclass(frozen=True)
class DatasetSighting:
    local_track_id: str
    camera_id: str
    zone_id: str
    timestamp: datetime
    embedding: Any
    object_class: str


@dataclass(frozen=True)
class DatasetIdentity:
    identity_id: str
    object_class: str
    cross_zone: bool
    sightings: tuple[DatasetSighting, ...]


@dataclass(frozen=True)
class SyntheticDataset:
    metadata: dict[str, Any]
    camera_order: tuple[str, ...]
    identities: tuple[DatasetIdentity, ...]
