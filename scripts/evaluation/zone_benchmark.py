def parse_zone_sizes(raw_value: str) -> list[int]:
    zone_sizes: list[int] = []
    for part in raw_value.split(","):
        stripped = part.strip()
        if not stripped:
            continue
        try:
            zone_size = int(stripped)
        except ValueError as exc:
            raise RuntimeError(f"invalid zone size {stripped!r}") from exc
        if zone_size <= 0:
            raise RuntimeError("zone sizes must be positive integers")
        zone_sizes.append(zone_size)
    if not zone_sizes:
        raise RuntimeError("--zone-sizes must contain at least one positive integer")
    return sorted(set(zone_sizes))


def parse_timestamp(value: Any) -> datetime:
    if not isinstance(value, str) or not value.strip():
        raise RuntimeError("sighting timestamp must be a non-empty ISO-8601 string")
    try:
        timestamp = datetime.fromisoformat(value.replace("Z", "+00:00"))
    except ValueError as exc:
        raise RuntimeError(f"invalid timestamp {value!r}") from exc
    if timestamp.tzinfo is None:
        raise RuntimeError(f"timestamp must include a timezone offset: {value!r}")
    return timestamp


def load_dataset(path: Path) -> SyntheticDataset:
    if not path.exists():
        raise RuntimeError(f"dataset not found: {path}")
    payload = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise RuntimeError("dataset JSON must be a top-level object")

    metadata = payload.get("metadata")
    if not isinstance(metadata, dict):
        raise RuntimeError("dataset metadata must be an object")

    camera_order_raw = payload.get("camera_order")
    if not isinstance(camera_order_raw, list) or not camera_order_raw:
        raise RuntimeError("dataset camera_order must be a non-empty list")
    camera_order: tuple[str, ...] = tuple(_require_non_empty_string(camera_id, "camera_id") for camera_id in camera_order_raw)
    camera_set = set(camera_order)

    identities_raw = payload.get("identities")
    if not isinstance(identities_raw, list) or not identities_raw:
        raise RuntimeError("dataset identities must be a non-empty list")

    numpy = require_module("numpy", "numpy")
    identities: list[DatasetIdentity] = []
    ground_truth_groups: list[IdentityGroup] = []

    for identity_payload in identities_raw:
        if not isinstance(identity_payload, dict):
            raise RuntimeError("each identity entry must be an object")
        identity_id = _require_non_empty_string(identity_payload.get("identity_id"), "identity_id")
        object_class = _require_non_empty_string(identity_payload.get("object_class"), "object_class")
        cross_zone = bool(identity_payload.get("cross_zone", False))
        sightings_raw = identity_payload.get("sightings")
        if not isinstance(sightings_raw, list) or len(sightings_raw) < 2:
            raise RuntimeError(f"identity {identity_id!r} must contain at least two sightings")
        sightings: list[DatasetSighting] = []
        gt_sightings: list[IdentitySighting] = []
        for sighting_payload in sightings_raw:
            if not isinstance(sighting_payload, dict):
                raise RuntimeError(f"identity {identity_id!r} contains a non-object sighting")
            local_track_id = _require_non_empty_string(
                sighting_payload.get("local_track_id"),
                "local_track_id",
            )
            camera_id = _require_non_empty_string(sighting_payload.get("camera_id"), "camera_id")
            if camera_id not in camera_set:
                raise RuntimeError(f"identity {identity_id!r} references unknown camera {camera_id!r}")
            zone_id = _require_non_empty_string(sighting_payload.get("zone_id"), "zone_id")
            embedding = sighting_payload.get("embedding")
            if not isinstance(embedding, list) or not embedding:
                raise RuntimeError(f"identity {identity_id!r} has an empty embedding")
            vector = numpy.asarray(embedding, dtype=numpy.float32)
            norm = float(numpy.linalg.norm(vector))
            if norm == 0.0:
                raise RuntimeError(f"identity {identity_id!r} contains a zero-norm embedding")
            vector = vector / norm
            timestamp_raw = parse_timestamp(sighting_payload.get("timestamp"))
            sightings.append(
                DatasetSighting(
                    local_track_id=local_track_id,
                    camera_id=camera_id,
                    zone_id=zone_id,
                    timestamp=timestamp_raw,
                    embedding=vector,
                    object_class=object_class,
                )
            )
            gt_sightings.append(
                IdentitySighting(
                    local_track_id=local_track_id,
                    camera_id=camera_id,
                    timestamp=timestamp_raw.isoformat(),
                    object_class=object_class,
                )
            )
        identities.append(
            DatasetIdentity(
                identity_id=identity_id,
                object_class=object_class,
                cross_zone=cross_zone,
                sightings=tuple(sorted(sightings, key=lambda item: (item.timestamp, item.local_track_id))),
            )
        )
        ground_truth_groups.append(
            IdentityGroup(global_id=identity_id, sightings=tuple(gt_sightings))
        )

    return SyntheticDataset(
        metadata=metadata,
        camera_order=camera_order,
        identities=tuple(identities),
        ground_truth=tuple(ground_truth_groups),
    )


def build_full_site_layout(camera_order: tuple[str, ...]) -> ShardingLayout:
    return ShardingLayout(
        label="full-site",
        zone_size=len(camera_order),
        camera_to_zone={camera_id: "full-site" for camera_id in camera_order},
        zone_cameras={"full-site": tuple(camera_order)},
        adjacent_zones={"full-site": tuple()},
        boundary_cameras=set(),
    )


def build_sharding_layout(camera_order: tuple[str, ...], zone_size: int) -> ShardingLayout:
    if zone_size >= len(camera_order):
        return build_full_site_layout(camera_order)

    zone_cameras: dict[str, tuple[str, ...]] = {}
    camera_to_zone: dict[str, str] = {}
    chunks: list[tuple[str, tuple[str, ...]]] = []
    for zone_index, start in enumerate(range(0, len(camera_order), zone_size)):
        chunk = tuple(camera_order[start : start + zone_size])
        zone_id = f"zone-{zone_size:02d}-{zone_index:02d}"
        zone_cameras[zone_id] = chunk
        chunks.append((zone_id, chunk))
        for camera_id in chunk:
            camera_to_zone[camera_id] = zone_id

    adjacent_zones: dict[str, tuple[str, ...]] = {}
    boundary_cameras: set[str] = set()
    for index, (zone_id, cameras) in enumerate(chunks):
        neighbors: list[str] = []
        if index > 0:
            neighbors.append(chunks[index - 1][0])
            boundary_cameras.add(cameras[0])
        if index < len(chunks) - 1:
            neighbors.append(chunks[index + 1][0])
            boundary_cameras.add(cameras[-1])
        adjacent_zones[zone_id] = tuple(neighbors)

    return ShardingLayout(
        label=f"zone-{zone_size}",
        zone_size=zone_size,
        camera_to_zone=camera_to_zone,
        zone_cameras=zone_cameras,
        adjacent_zones=adjacent_zones,
        boundary_cameras=boundary_cameras,
    )


def flatten_sightings(dataset: SyntheticDataset) -> list[DatasetSighting]:
    sightings = [
        sighting
        for identity in dataset.identities
        for sighting in identity.sightings
    ]
    return sorted(sightings, key=lambda item: (item.timestamp, item.local_track_id))


def search_candidates(
    *,
    query_vector: Any,
    candidates: list[MatchingState],
) -> tuple[MatchingState | None, float, float, int]:
    numpy = require_module("numpy", "numpy")
    if not candidates:
        return None, 0.0, 0.0, 0

    candidate_vectors = numpy.stack([state.sighting.embedding for state in candidates])
    start = time.perf_counter()
    scores = candidate_vectors @ query_vector
    elapsed_ms = (time.perf_counter() - start) * 1000.0
    best_index = int(numpy.argmax(scores))
    return candidates[best_index], float(scores[best_index]), elapsed_ms, len(candidates)


def summarize_latencies(latencies_ms: list[float], candidate_counts: list[int]) -> LatencySummary:
    numpy = require_module("numpy", "numpy")
    if not latencies_ms:
        return LatencySummary(
            mean_ms=0.0,
            p50_ms=0.0,
            p95_ms=0.0,
            p99_ms=0.0,
            total_queries=0,
            average_candidates=0.0,
        )
    latency_array = numpy.asarray(latencies_ms, dtype=numpy.float64)
    candidate_array = numpy.asarray(candidate_counts, dtype=numpy.float64)
    return LatencySummary(
        mean_ms=float(latency_array.mean()),
        p50_ms=float(numpy.percentile(latency_array, 50)),
        p95_ms=float(numpy.percentile(latency_array, 95)),
        p99_ms=float(numpy.percentile(latency_array, 99)),
        total_queries=len(latencies_ms),
        average_candidates=float(candidate_array.mean()) if candidate_counts else 0.0,
    )


def embedding_bytes(vector_count: int, embedding_dim: int) -> int:
    return int(vector_count * embedding_dim * 4)


def benchmark_full_site(
    dataset: SyntheticDataset,
    match_threshold: float,
) -> ScenarioResult:
    sightings = flatten_sightings(dataset)
    states: list[MatchingState] = []
    predictions: list[PredictedAssociation] = []
    latencies_ms: list[float] = []
    candidate_counts: list[int] = []
    next_group_id = 1
    embedding_dim = int(dataset.metadata.get("embedding_dim", 512))
    peak_total_vectors = 0

    for sighting in sightings:
        class_candidates = [
            state
            for state in states
            if state.sighting.object_class == sighting.object_class
        ]
        best_state, best_score, latency_ms, candidate_count = search_candidates(
            query_vector=sighting.embedding,
            candidates=class_candidates,
        )
        latencies_ms.append(latency_ms)
        candidate_counts.append(candidate_count)

        if best_state is not None and best_score >= match_threshold:
            global_track_id = best_state.global_track_id
            confidence = best_score
        else:
            global_track_id = f"full-{next_group_id:06d}"
            confidence = 1.0
            next_group_id += 1

        states.append(MatchingState(sighting=sighting, global_track_id=global_track_id))
        predictions.append(
            PredictedAssociation(
                local_track_id=sighting.local_track_id,
                global_track_id=global_track_id,
                camera_id=sighting.camera_id,
                confidence=confidence,
                linked_at=sighting.timestamp.isoformat(),
                object_class=sighting.object_class,
            )
        )
        peak_total_vectors = max(peak_total_vectors, len(states))

    metrics = compute_reid_metrics(list(dataset.ground_truth), predictions)
    boundary_metrics = compute_reid_metrics([], predictions)
    latency = summarize_latencies(latencies_ms, candidate_counts)
    memory = MemorySummary(
        peak_total_index_bytes=embedding_bytes(peak_total_vectors, embedding_dim),
        peak_shard_index_bytes=embedding_bytes(peak_total_vectors, embedding_dim),
        peak_boundary_index_bytes=0,
        peak_total_vectors=peak_total_vectors,
        peak_shard_vectors=peak_total_vectors,
        peak_boundary_vectors=0,
    )
    return ScenarioResult(
        name="full-site",
        mode="full-site",
        zone_size=len(dataset.camera_order),
        zone_count=1,
        metrics=metrics,
        boundary_metrics=boundary_metrics,
        latency=latency,
        memory=memory,
    )


def select_cross_zone_best(
    *,
    sighting: DatasetSighting,
    zone_id: str,
    layout: ShardingLayout,
    boundary_states_by_zone: dict[str, list[MatchingState]],
    cross_zone_threshold: float,
) -> tuple[MatchingState | None, float, float, int]:
    best_match: MatchingState | None = None
    best_score = 0.0
    total_latency_ms = 0.0
    total_candidates = 0

    for adjacent_zone in layout.adjacent_zones.get(zone_id, ()):
        candidate_pool = [
            state
            for state in boundary_states_by_zone.get(adjacent_zone, [])
            if state.sighting.object_class == sighting.object_class
        ]
        candidate, score, latency_ms, candidate_count = search_candidates(
            query_vector=sighting.embedding,
            candidates=candidate_pool,
        )
        total_latency_ms += latency_ms
        total_candidates += candidate_count
        if candidate is not None and score >= cross_zone_threshold and score > best_score:
            best_match = candidate
            best_score = score

    return best_match, best_score, total_latency_ms, total_candidates


def filter_boundary_groups(
    ground_truth: tuple[IdentityGroup, ...],
    layout: ShardingLayout,
) -> list[IdentityGroup]:
    boundary_groups: list[IdentityGroup] = []
    for group in ground_truth:
        zone_ids = {
            layout.camera_to_zone[sighting.camera_id]
            for sighting in group.sightings
            if sighting.camera_id in layout.camera_to_zone
        }
        if len(zone_ids) >= 2:
            boundary_groups.append(group)
    return boundary_groups


def benchmark_zone_sharded(
    dataset: SyntheticDataset,
    *,
    layout: ShardingLayout,
    match_threshold: float,
    cross_zone_threshold: float,
) -> ScenarioResult:
    sightings = flatten_sightings(dataset)
    states_by_zone: dict[str, list[MatchingState]] = {
        zone_id: [] for zone_id in layout.zone_cameras
    }
    boundary_states_by_zone: dict[str, list[MatchingState]] = {
        zone_id: [] for zone_id in layout.zone_cameras
    }
    predictions: list[PredictedAssociation] = []
    latencies_ms: list[float] = []
    candidate_counts: list[int] = []
    next_group_id = 1
    embedding_dim = int(dataset.metadata.get("embedding_dim", 512))
    peak_total_vectors = 0
    peak_shard_vectors = 0
    peak_boundary_vectors = 0

    for sighting in sightings:
        zone_id = layout.camera_to_zone[sighting.camera_id]
        local_candidates = [
            state
            for state in states_by_zone[zone_id]
            if state.sighting.object_class == sighting.object_class
        ]
        best_state, best_score, intra_latency_ms, intra_candidate_count = search_candidates(
            query_vector=sighting.embedding,
            candidates=local_candidates,
        )
        total_latency_ms = intra_latency_ms
        total_candidate_count = intra_candidate_count

        if best_state is not None and best_score >= match_threshold:
            matched_state = best_state
            confidence = best_score
        else:
            matched_state = None
            confidence = 0.0
            if sighting.camera_id in layout.boundary_cameras:
                cross_match, cross_score, cross_latency_ms, cross_candidate_count = (
                    select_cross_zone_best(
                        sighting=sighting,
                        zone_id=zone_id,
                        layout=layout,
                        boundary_states_by_zone=boundary_states_by_zone,
                        cross_zone_threshold=cross_zone_threshold,
                    )
                )
                total_latency_ms += cross_latency_ms
                total_candidate_count += cross_candidate_count
                if cross_match is not None:
                    matched_state = cross_match
                    confidence = cross_score

        latencies_ms.append(total_latency_ms)
        candidate_counts.append(total_candidate_count)

        if matched_state is not None:
            global_track_id = matched_state.global_track_id
        else:
            global_track_id = f"{layout.label}-{next_group_id:06d}"
            confidence = 1.0
            next_group_id += 1

        state = MatchingState(sighting=sighting, global_track_id=global_track_id)
        states_by_zone[zone_id].append(state)
        if sighting.camera_id in layout.boundary_cameras:
            boundary_states_by_zone[zone_id].append(state)

        predictions.append(
            PredictedAssociation(
                local_track_id=sighting.local_track_id,
                global_track_id=global_track_id,
                camera_id=sighting.camera_id,
                confidence=confidence,
                linked_at=sighting.timestamp.isoformat(),
                object_class=sighting.object_class,
            )
        )

        total_vectors = sum(len(items) for items in states_by_zone.values())
        max_shard = max((len(items) for items in states_by_zone.values()), default=0)
        boundary_vectors = sum(len(items) for items in boundary_states_by_zone.values())
        peak_total_vectors = max(peak_total_vectors, total_vectors)
        peak_shard_vectors = max(peak_shard_vectors, max_shard)
        peak_boundary_vectors = max(peak_boundary_vectors, boundary_vectors)

    metrics = compute_reid_metrics(list(dataset.ground_truth), predictions)
    boundary_groups = filter_boundary_groups(dataset.ground_truth, layout)
    boundary_metrics = compute_reid_metrics(boundary_groups, predictions)
    latency = summarize_latencies(latencies_ms, candidate_counts)
    memory = MemorySummary(
        peak_total_index_bytes=embedding_bytes(peak_total_vectors, embedding_dim),
        peak_shard_index_bytes=embedding_bytes(peak_shard_vectors, embedding_dim),
        peak_boundary_index_bytes=embedding_bytes(peak_boundary_vectors, embedding_dim),
        peak_total_vectors=peak_total_vectors,
        peak_shard_vectors=peak_shard_vectors,
        peak_boundary_vectors=peak_boundary_vectors,
    )
    return ScenarioResult(
        name=layout.label,
        mode="zone-sharded",
        zone_size=layout.zone_size,
        zone_count=len(layout.zone_cameras),
        metrics=metrics,
        boundary_metrics=boundary_metrics,
        latency=latency,
        memory=memory,
    )


def scenario_to_json_dict(
    scenario: ScenarioResult,
    full_site: ScenarioResult | None = None,
) -> dict[str, Any]:
    payload = {
        "name": scenario.name,
        "mode": scenario.mode,
        "zone_size": scenario.zone_size,
        "zone_count": scenario.zone_count,
        "metrics": metrics_to_json_dict(scenario.metrics),
        "boundary_metrics": metrics_to_json_dict(scenario.boundary_metrics),
        "latency": {
            "mean_ms": scenario.latency.mean_ms,
            "p50_ms": scenario.latency.p50_ms,
            "p95_ms": scenario.latency.p95_ms,
            "p99_ms": scenario.latency.p99_ms,
            "total_queries": scenario.latency.total_queries,
            "average_candidates": scenario.latency.average_candidates,
        },
        "memory": {
            "peak_total_index_bytes": scenario.memory.peak_total_index_bytes,
            "peak_shard_index_bytes": scenario.memory.peak_shard_index_bytes,
            "peak_boundary_index_bytes": scenario.memory.peak_boundary_index_bytes,
            "peak_total_vectors": scenario.memory.peak_total_vectors,
            "peak_shard_vectors": scenario.memory.peak_shard_vectors,
            "peak_boundary_vectors": scenario.memory.peak_boundary_vectors,
        },
    }
    if full_site is not None:
        payload["deltas_vs_full_site"] = {
            "rank1_accuracy": scenario.metrics.rank1_accuracy - full_site.metrics.rank1_accuracy,
            "mean_average_precision": (
                scenario.metrics.mean_average_precision
                - full_site.metrics.mean_average_precision
            ),
            "false_positive_rate": (
                scenario.metrics.false_positive_rate
                - full_site.metrics.false_positive_rate
            ),
            "latency_p95_ms": scenario.latency.p95_ms - full_site.latency.p95_ms,
            "peak_shard_index_bytes": (
                scenario.memory.peak_shard_index_bytes
                - full_site.memory.peak_shard_index_bytes
            ),
        }
    return payload


def detect_git_state() -> tuple[str | None, bool | None]:
    try:
        revision = subprocess.check_output(
            ["git", "rev-parse", "HEAD"],
            stderr=subprocess.DEVNULL,
            text=True,
        ).strip()
        dirty = bool(
            subprocess.check_output(
                ["git", "status", "--porcelain"],
                stderr=subprocess.DEVNULL,
                text=True,
            ).strip()
        )
        return revision, dirty
    except (OSError, subprocess.CalledProcessError):  # pragma: no cover - environment dependent
        return None, None


def sanitize_metric_name(value: str) -> str:
    return "".join(character if character.isalnum() else "_" for character in value)


def boundary_cross_zone_group_count(
    ground_truth: tuple[IdentityGroup, ...],
    layout: ShardingLayout,
) -> int:
    return len(filter_boundary_groups(ground_truth, layout))


def format_bytes(value: int) -> str:
    units = ("B", "KB", "MB", "GB")
    result = float(value)
    unit = units[0]
    for unit in units:
        if result < 1024.0 or unit == units[-1]:
            break
        result /= 1024.0
    return f"{result:.2f} {unit}"


def build_recommendation(
    full_site: ScenarioResult,
    sharded_results: list[ScenarioResult],
) -> str:
    if not sharded_results:
        return "No sharded scenarios were executed."
    acceptable = [
        scenario
        for scenario in sharded_results
        if scenario.metrics.rank1_accuracy >= full_site.metrics.rank1_accuracy - 0.02
        and scenario.metrics.mean_average_precision
        >= full_site.metrics.mean_average_precision - 0.02
    ]
    if not acceptable:
        return (
            "NO-GO for zone sharding with the tested partitions: every sharded layout "
            "regressed Rank-1 or mAP by more than 2 percentage points versus full-site."
        )
    best = min(acceptable, key=lambda scenario: scenario.latency.p95_ms)
    return (
        f"GO for zone sharding at `{best.zone_size}` cameras/zone: it stayed within the "
        "2-point quality tolerance and delivered the best p95 search latency among "
        "the acceptable layouts."
    )


def build_report_markdown(
    *,
    dataset_path: Path,
    dataset: SyntheticDataset,
    full_site: ScenarioResult,
    sharded_results: list[ScenarioResult],
    mlflow_run_id: str,
    match_threshold: float,
    cross_zone_threshold: float,
) -> str:
    metadata = dataset.metadata
    lines = [
        "# Zone Benchmark Report",
        "",
        "## Configuration Summary",
        "",
        f"- Dataset: `{dataset_path}`",
        f"- Cameras: `{len(dataset.camera_order)}`",
        f"- Identities: `{metadata.get('identity_count', len(dataset.identities))}`",
        f"- Cross-zone fraction: `{metadata.get('cross_zone_fraction', 'n/a')}`",
        f"- Embedding dimension: `{metadata.get('embedding_dim', 'n/a')}`",
        f"- MLflow run ID: `{mlflow_run_id}`",
        "",
        "## Accuracy Comparison",
        "",
        "| Scenario | Zones | Cameras/Zone | Rank-1 | Rank-5 | mAP | Precision | Recall | FPR | Delta vs Full Rank-1 |",
        "|----------|-------|--------------|--------|--------|-----|-----------|--------|-----|----------------------|",
        (
            f"| {full_site.name} | {full_site.zone_count} | {full_site.zone_size} | "
            f"{full_site.metrics.rank1_accuracy:.4f} | {full_site.metrics.rank5_accuracy:.4f} | "
            f"{full_site.metrics.mean_average_precision:.4f} | {full_site.metrics.precision:.4f} | "
            f"{full_site.metrics.recall:.4f} | {full_site.metrics.false_positive_rate:.4f} | baseline |"
        ),
    ]
    for scenario in sharded_results:
        lines.append(
            f"| {scenario.name} | {scenario.zone_count} | {scenario.zone_size} | "
            f"{scenario.metrics.rank1_accuracy:.4f} | {scenario.metrics.rank5_accuracy:.4f} | "
            f"{scenario.metrics.mean_average_precision:.4f} | {scenario.metrics.precision:.4f} | "
            f"{scenario.metrics.recall:.4f} | {scenario.metrics.false_positive_rate:.4f} | "
            f"{scenario.metrics.rank1_accuracy - full_site.metrics.rank1_accuracy:+.4f} |"
        )

    lines.extend(
        [
            "",
            "## Cross-Zone Boundary Accuracy",
            "",
            "| Scenario | Boundary Identity Groups | Rank-1 | Rank-5 | mAP | Precision | Recall |",
            "|----------|--------------------------|--------|--------|-----|-----------|--------|",
        ]
    )
    for scenario in sharded_results:
        boundary_group_count = boundary_cross_zone_group_count(dataset.ground_truth, build_sharding_layout(dataset.camera_order, scenario.zone_size))
        lines.append(
            f"| {scenario.name} | {boundary_group_count} | "
            f"{scenario.boundary_metrics.rank1_accuracy:.4f} | "
            f"{scenario.boundary_metrics.rank5_accuracy:.4f} | "
            f"{scenario.boundary_metrics.mean_average_precision:.4f} | "
            f"{scenario.boundary_metrics.precision:.4f} | "
            f"{scenario.boundary_metrics.recall:.4f} |"
        )

    lines.extend(
        [
            "",
            "## Latency Scaling",
            "",
            "| Scenario | Mean Search ms | p50 ms | p95 ms | p99 ms | Avg Candidates/Query |",
            "|----------|----------------|--------|--------|--------|----------------------|",
            f"| {full_site.name} | {full_site.latency.mean_ms:.6f} | {full_site.latency.p50_ms:.6f} | {full_site.latency.p95_ms:.6f} | {full_site.latency.p99_ms:.6f} | {full_site.latency.average_candidates:.2f} |",
        ]
    )
    for scenario in sharded_results:
        lines.append(
            f"| {scenario.name} | {scenario.latency.mean_ms:.6f} | {scenario.latency.p50_ms:.6f} | "
            f"{scenario.latency.p95_ms:.6f} | {scenario.latency.p99_ms:.6f} | "
            f"{scenario.latency.average_candidates:.2f} |"
        )

    lines.extend(
        [
            "",
            "## Memory Scaling",
            "",
            "| Scenario | Peak Total Index | Peak Largest Shard | Peak Boundary Index | Peak Largest Shard vs Full |",
            "|----------|------------------|--------------------|---------------------|----------------------------|",
            f"| {full_site.name} | {format_bytes(full_site.memory.peak_total_index_bytes)} | {format_bytes(full_site.memory.peak_shard_index_bytes)} | {format_bytes(full_site.memory.peak_boundary_index_bytes)} | baseline |",
        ]
    )
    for scenario in sharded_results:
        ratio = (
            scenario.memory.peak_shard_index_bytes / full_site.memory.peak_shard_index_bytes
            if full_site.memory.peak_shard_index_bytes > 0
            else 0.0
        )
        lines.append(
            f"| {scenario.name} | {format_bytes(scenario.memory.peak_total_index_bytes)} | "
            f"{format_bytes(scenario.memory.peak_shard_index_bytes)} | "
            f"{format_bytes(scenario.memory.peak_boundary_index_bytes)} | "
            f"{ratio:.4f}x |"
        )

    lines.extend(
        [
            "",
            "## Recommendations",
            "",
            build_recommendation(full_site, sharded_results),
            "",
            "## Notes",
            "",
            "- This benchmark is a deterministic offline proxy. It measures cosine-search cost and assignment quality, not the full live Kafka/DB service path.",
            f"- Full-site matching uses one global search pool with the {match_threshold:.2f} threshold.",
            f"- Zone-sharded matching uses the same {match_threshold:.2f} intra-zone threshold plus a {cross_zone_threshold:.2f} cross-zone threshold on boundary-camera searches only.",
            "- Boundary accuracy is computed on the subset of identity groups that span multiple benchmark zones for the tested partition size.",
        ]
    )
    return "\n".join(lines) + "\n"


def write_local_artifacts(
    *,
    args: argparse.Namespace,
    dataset: SyntheticDataset,
    full_site: ScenarioResult,
    sharded_results: list[ScenarioResult],
    mlflow_run_id: str,
) -> tuple[Path, Path]:
    args.output_dir.mkdir(parents=True, exist_ok=True)
    results_path = args.output_dir / "benchmark_results.json"
    report_path = args.output_dir / "zone_benchmark_report.md"
    payload = {
        "dataset": {
            "path": str(args.dataset),
            "metadata": dataset.metadata,
            "camera_count": len(dataset.camera_order),
            "identity_count": len(dataset.identities),
        },
        "thresholds": {
            "match_threshold": args.match_threshold,
            "cross_zone_threshold": args.cross_zone_threshold,
        },
        "full_site": scenario_to_json_dict(full_site),
        "zone_sharded": [
            scenario_to_json_dict(scenario, full_site=full_site)
            for scenario in sharded_results
        ],
        "mlflow_run_id": mlflow_run_id,
    }
    results_path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    report_path.write_text(
        build_report_markdown(
            dataset_path=args.dataset,
            dataset=dataset,
            full_site=full_site,
            sharded_results=sharded_results,
            mlflow_run_id=mlflow_run_id,
            match_threshold=args.match_threshold,
            cross_zone_threshold=args.cross_zone_threshold,
        ),
        encoding="utf-8",
    )
    return results_path, report_path


def log_to_mlflow(
    *,
    args: argparse.Namespace,
    dataset: SyntheticDataset,
    full_site: ScenarioResult,
    sharded_results: list[ScenarioResult],
) -> str:
    mlflow = require_module("mlflow", "mlflow")
    mlflow.set_tracking_uri(args.mlflow_uri)
    mlflow.set_experiment(args.mlflow_experiment)
    git_revision, git_dirty = detect_git_state()

    with tempfile.TemporaryDirectory(prefix="zone-benchmark-") as temp_dir_name:
        temp_dir = Path(temp_dir_name)
        temp_results = temp_dir / "benchmark_results.json"
        temp_report = temp_dir / "zone_benchmark_report.md"

        with mlflow.start_run(
            run_name=f"zone-benchmark-{len(dataset.camera_order)}cams"
        ) as run:
            mlflow.set_tags(
                {
                    "eval.phase": "zone-benchmark",
                    "benchmark.kind": "mtmc-zone-sharding",
                }
            )
            params: dict[str, Any] = {
                "dataset_path": str(args.dataset),
                "camera_count": len(dataset.camera_order),
                "identity_count": len(dataset.identities),
                "zone_sizes": ",".join(str(value) for value in parse_zone_sizes(args.zone_sizes)),
                "match_threshold": args.match_threshold,
                "cross_zone_threshold": args.cross_zone_threshold,
                "seed": args.seed,
            }
            if git_revision is not None:
                params["git_revision"] = git_revision
            if git_dirty is not None:
                params["git_dirty"] = str(git_dirty).lower()
            mlflow.log_params(params)

            payload = {
                "dataset": {
                    "path": str(args.dataset),
                    "metadata": dataset.metadata,
                    "camera_count": len(dataset.camera_order),
                    "identity_count": len(dataset.identities),
                },
                "thresholds": {
                    "match_threshold": args.match_threshold,
                    "cross_zone_threshold": args.cross_zone_threshold,
                },
                "full_site": scenario_to_json_dict(full_site),
                "zone_sharded": [
                    scenario_to_json_dict(scenario, full_site=full_site)
                    for scenario in sharded_results
                ],
                "mlflow_run_id": run.info.run_id,
            }
            temp_results.write_text(
                json.dumps(payload, indent=2, sort_keys=True) + "\n",
                encoding="utf-8",
            )
            temp_report.write_text(
                build_report_markdown(
                    dataset_path=args.dataset,
                    dataset=dataset,
                    full_site=full_site,
                    sharded_results=sharded_results,
                    mlflow_run_id=run.info.run_id,
                    match_threshold=args.match_threshold,
                    cross_zone_threshold=args.cross_zone_threshold,
                ),
                encoding="utf-8",
            )

            mlflow.log_metrics(
                {
                    "full_site_rank1_accuracy": full_site.metrics.rank1_accuracy,
                    "full_site_rank5_accuracy": full_site.metrics.rank5_accuracy,
                    "full_site_map": full_site.metrics.mean_average_precision,
                    "full_site_false_positive_rate": full_site.metrics.false_positive_rate,
                    "full_site_latency_mean_ms": full_site.latency.mean_ms,
                    "full_site_latency_p95_ms": full_site.latency.p95_ms,
                    "full_site_peak_index_bytes": float(full_site.memory.peak_shard_index_bytes),
                }
            )

            for scenario in sharded_results:
                prefix = sanitize_metric_name(scenario.name)
                mlflow.log_metrics(
                    {
                        f"{prefix}_rank1_accuracy": scenario.metrics.rank1_accuracy,
                        f"{prefix}_rank5_accuracy": scenario.metrics.rank5_accuracy,
                        f"{prefix}_map": scenario.metrics.mean_average_precision,
                        f"{prefix}_false_positive_rate": scenario.metrics.false_positive_rate,
                        f"{prefix}_boundary_rank1_accuracy": scenario.boundary_metrics.rank1_accuracy,
                        f"{prefix}_boundary_map": scenario.boundary_metrics.mean_average_precision,
                        f"{prefix}_latency_mean_ms": scenario.latency.mean_ms,
                        f"{prefix}_latency_p95_ms": scenario.latency.p95_ms,
                        f"{prefix}_peak_shard_index_bytes": float(
                            scenario.memory.peak_shard_index_bytes
                        ),
                        f"{prefix}_peak_boundary_index_bytes": float(
                            scenario.memory.peak_boundary_index_bytes
                        ),
                        f"{prefix}_rank1_delta_vs_full": (
                            scenario.metrics.rank1_accuracy - full_site.metrics.rank1_accuracy
                        ),
                        f"{prefix}_map_delta_vs_full": (
                            scenario.metrics.mean_average_precision
                            - full_site.metrics.mean_average_precision
                        ),
                    }
                )

            mlflow.log_artifact(str(temp_results))
            mlflow.log_artifact(str(temp_report))
            return run.info.run_id


def _require_non_empty_string(value: Any, field_name: str) -> str:
    if not isinstance(value, str) or not value.strip():
        raise RuntimeError(f"{field_name} must be a non-empty string")
    return value.strip()


def benchmark(args: argparse.Namespace) -> dict[str, Any]:
    dataset = load_dataset(args.dataset)
    full_site = benchmark_full_site(dataset, args.match_threshold)

    zone_sizes = parse_zone_sizes(args.zone_sizes)
    sharded_results: list[ScenarioResult] = []
    for zone_size in zone_sizes:
        layout = build_sharding_layout(dataset.camera_order, zone_size)
        if layout.label == "full-site":
            continue
        sharded_results.append(
            benchmark_zone_sharded(
                dataset,
                layout=layout,
                match_threshold=args.match_threshold,
                cross_zone_threshold=args.cross_zone_threshold,
            )
        )

    mlflow_run_id = log_to_mlflow(
        args=args,
        dataset=dataset,
        full_site=full_site,
        sharded_results=sharded_results,
    )
    results_path, report_path = write_local_artifacts(
        args=args,
        dataset=dataset,
        full_site=full_site,
        sharded_results=sharded_results,
        mlflow_run_id=mlflow_run_id,
    )
    return {
        "dataset_path": str(args.dataset),
        "results_path": str(results_path),
        "report_path": str(report_path),
        "mlflow_run_id": mlflow_run_id,
        "full_site_rank1_accuracy": full_site.metrics.rank1_accuracy,
        "zone_sizes_tested": zone_sizes,
        "best_sharded_rank1_accuracy": max(
            (scenario.metrics.rank1_accuracy for scenario in sharded_results),
            default=0.0,
        ),
    }


def main() -> None:
    args = parse_args()
    summary = benchmark(args)
    print(json.dumps(summary))


if __name__ == "__main__":
    try:
        main()
    except Exception as exc:
        raise SystemExit(str(exc)) from exc
